package exosWRSerie2;

public class Exo2Const {

		private  String source;
		private  String dest;
		public Exo2Const(String source, String dest) {
			this.source=source;
			this.dest=dest;
		}
		

		public String getSource() {
			return source;
		}

		public String getDest() {
			return dest;
		}
}
